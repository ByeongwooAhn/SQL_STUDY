SELECT *
    FROM EMP;

-- 정렬하고자 할 때는 ORDER BY절을 사용하면 된다. 쿼리문에서 항상 제일 마지막에 실행된다.
SELECT *
    FROM EMP
ORDER BY DEPTNO DESC, JOB;

-- 데이터의 중복을 제거하는 DISTINCT문
SELECT DISTINCT DEPTNO
    FROM EMP
    ORDER BY DEPTNO;
    
-- ALIAS별칭
SELECT DEPTNO AS "부서번호"
    FROM EMP;
    
-- 비교 논리 연산자 사용
SELECT *
    FROM EMP
WHERE DEPTNO = 30
    AND SAL > 2000;
    
-- LIKE문(와일드 카드 -- % 혹은 _)
SELECT *
    FROM EMP
WHERE ENAME LIKE 'TEST1%';

SELECT *
    FROM EMP
WHERE ENAME LIKE '%1';

SELECT *
    FROM EMP
WHERE ENAME LIKE 'TEST__';

-- BETWEEN ~ AND: 지정된 범위에 있는 값을 조회(연속적인 값)
SELECT *
    FROM EMP
WHERE SAL BETWEEN 1000 AND 2000;  -- 1000이상 2000 이하

SELECT *
    FROM EMP
WHERE SAL NOT BETWEEN 1000 AND 2000;  -- 1000미만 2000 초과

-- IN(): OR의 의미를 지닌다.
SELECT *
    FROM EMP
WHERE JOB IN('CLERK', 'MANAGER');

SELECT *
    FROM EMP
WHERE JOB = 'CLERK'
        OR JOB = 'MANAGER';

SELECT *
    FROM EMP
WHERE JOB NOT IN('CLERK', 'MANAGER');