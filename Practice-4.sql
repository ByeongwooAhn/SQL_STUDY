DROP TABLE EMP;
CREATE TABLE EMP(
    EMPNO INT NOT NULL PRIMARY KEY,
    ENAME VARCHAR(30) NOT NULL,
    DEPTNO NUMBER(5) NOT NULL,
    MGR NUMBER(5),
    JOB VARCHAR2(20) NOT NULL,
    SAL INT NOT NULL
);

